def formatTitle(title):
    new_title = ''
    for ch in title:
        if ch in 'aáàâãäåbcçdeéèêëfghiíìîïjklmnoóòôõöpqrstuúùûüvwxyzAÁÀÂÃÄÅBCÇDEÉÈÊËFGHIÍÌÎÏJKLMNOÓÒÔÕÖPQRSTUÚÙÛÜVWXYZ0123456789-_()[]{}# ':
            new_title += ch
    new_title = sub(' +', ' ', new_title)
    new_title = new_title.strip()
    return new_title

def downloadFfmpeg():
    mkdir(fr'{app_dir}\ffmpeg', exist_ok=True)
    ffmpeg_exe = Path(fr'{app_dir}\ffmpeg\ffmpeg.exe')

    if not ffmpeg_exe.is_file():
        ffmpeg_url = 'https://drive.google.com/uc?export=download&id=16Ob9qv7uwLWqcMOwTOKeC9p52accn-wO'
        r = reqget(ffmpeg_url, allow_redirects=True)
        open(fr'{app_dir}\ffmpeg\ffmpeg.exe.zst', 'wb').write(r.content)

        with open(fr'{app_dir}\ffmpeg\ffmpeg.exe.zst', mode='rb') as fi:
            with open(fr'{app_dir}\ffmpeg\ffmpeg.exe', mode='wb') as fo:
                fo.write(ZSTD_uncompress(fi.read()))

        remove(fr'{app_dir}\ffmpeg\ffmpeg.exe.zst')
    environ['PATH'] += pathsep + path.join(getcwd(), fr'{app_dir}\ffmpeg')
ProcDownloadFfmpeg = Process(target=downloadFfmpeg)
ProcDownloadFfmpeg.start()

def downloadAndSetThumbnail(self, formattedTitle, videoId):
    thumbnailPreviewFilename = '-[Thumbnail Preview]- ' + formattedTitle
    self.dV_imgThumbnailPreview.setScaledContents(True)
    self.dA_imgThumbnailPreview.setScaledContents(True)

    if path.exists(fr'{app_dir}\thumbnails\{thumbnailPreviewFilename}.jpg'):
        self.dV_imgThumbnailPreview.setPixmap(QtGui.QPixmap(fr'{app_dir}\thumbnails\{thumbnailPreviewFilename}.jpg'))
        self.dA_imgThumbnailPreview.setPixmap(QtGui.QPixmap(fr'{app_dir}\thumbnails\{thumbnailPreviewFilename}.jpg'))
    else:
        mkdir(fr'{app_dir}\thumbnails', exist_ok=True)
        r = reqget(f'https://img.youtube.com/vi/{videoId}/mqdefault.jpg', allow_redirects=True)
        open(fr'{app_dir}\thumbnails\{thumbnailPreviewFilename}.jpg', 'wb').write(r.content)

        self.dV_imgThumbnailPreview.setPixmap(QtGui.QPixmap(fr'{app_dir}\thumbnails\{thumbnailPreviewFilename}.jpg'))
        self.dA_imgThumbnailPreview.setPixmap(QtGui.QPixmap(fr'{app_dir}\thumbnails\{thumbnailPreviewFilename}.jpg'))
ProcDownloadAndSetThumbnail = Process(target=downloadAndSetThumbnail)

